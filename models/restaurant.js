class Restaurant {
  constructor(
    id,
    name,
    category,
    imageUrl,
    tel,
    address,
    price,
  ) {
    this.id = id;
    this.name = name;
    this.category = category;
    this.imageUrl = imageUrl;
    this.tel = tel;
    this.address = address;
    this.price = price;
  }
}

export default Restaurant;